/**
 * A city we publish {service}-{city}-ma pages for.
 *
 * SOURCED vs UNSOURCED — read before adding a city.
 *
 * Everything down to `density` comes from the US Census (ACS 2023 5-year +
 * 2023 Gazetteer) and is reproducible by re-running scripts/fetch-census.js.
 * These are facts we show to residents of the town, so they must be real.
 *
 * The fields below the line (architectureStyle, neighborhoods, challenges,
 * climate) do NOT exist in any public dataset. They are optional on purpose:
 * a city with real local knowledge behind it can fill them in, and a city
 * without simply omits them — the page hides those sections rather than
 * printing something invented. Never populate them by guessing; a wrong
 * neighborhood name is read by someone who lives there.
 */
export interface City {
  name: string;
  slug: string;
  // ---- Census-sourced (ACS 2023 5-year / 2023 Gazetteer) ----
  population: number;
  medianHomeValue: number;
  medianIncome?: number;
  /**
   * Share of housing units built before 1980, per ACS table B25034.
   * NOT the pre-1978 figure the EPA Lead-Safe rule keys on: the Census
   * bucket boundary is 1980, and there is no 1978 cut in the data. Copy must
   * say "before 1980" and may note that many such homes fall under the
   * pre-1978 rule — it must not present this number AS the pre-1978 share.
   */
  pre1980Percent?: number;
  /**
   * Median year the town's housing was built (ACS B25035). One concrete year,
   * more precise than the pre-1980 bucket above.
   */
  medianYearBuilt?: number;
  /** Share of occupied units that are owner-occupied (ACS B25003). */
  ownerOccupiedPercent?: number;
  /** Share of housing in 1-unit structures, detached or attached (ACS B25024). */
  singleFamilyPercent?: number;
  /**
   * Share of housing in 2-4 unit structures (ACS B25024) — the two- and
   * three-decker band, deliberately excluding large complexes this business
   * does not bid on.
   */
  smallMultiFamilyPercent?: number;
  county?: string;
  zipCodes?: string[];
  /** Population per square mile of land area. Drives `areaType`. */
  density?: number;
  /** Miles from the Hudson shop (74 Broad St), great-circle. */
  distanceMiles?: number;
  /**
   * Official Census centroid. Required to measure city-to-city distance —
   * `distanceMiles` cannot do it: two towns can both sit 15 miles from Hudson
   * and 30 miles from each other if they lie in opposite directions.
   */
  lat?: number;
  lon?: number;
  // ---- Derived from the above ----
  region: 'metrowest' | 'worcester-county' | 'greater-boston' | 'central-ma' | 'north-shore';
  areaType: 'urban' | 'suburban' | 'rural' | 'semi-rural';
  // ---- Local knowledge — NOT in any dataset. Omit unless actually known. ----
  architectureStyle?: string[];
  neighborhoods?: string[];
  challenges?: string[];
  climate?: string;
}

